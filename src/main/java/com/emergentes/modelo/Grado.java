
package com.emergentes.modelo;

public class Grado {
    private int id_grado;
    private String detalle_grado;

    public Grado() {
    }

    public int getId_grado() {
        return id_grado;
    }

    public void setId_grado(int id_grado) {
        this.id_grado = id_grado;
    }

    public String getDetalle_grado() {
        return detalle_grado;
    }

    public void setDetalle_grado(String detalle_grado) {
        this.detalle_grado = detalle_grado;
    }
    
    
}
